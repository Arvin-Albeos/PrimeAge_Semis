    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        String med = model.getValueAt(index,0).toString();
        String store = model.getValueAt(index,1).toString();
        String quan = model.getValueAt(index,2).toString();
        String total = model.getValueAt(index,3).toString();
        String paid = model.getValueAt(index,4).toString();
        
        history.setVisible(true);
        history.pack();
        history.setLocationRelativeTo(null);
        
        history.fMed.setText(med);
        history.fStore.setText(store);
        history.fQuan.setText(quan);
        history.fTotal.setText(total);
        history.fPaid.setText(paid);
    }//GEN-LAST:event_jTable1MouseClicked
    
